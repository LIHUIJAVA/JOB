package com.px.mis.account.entity.VoucherForU8;

public class U8VoucherSub {
	private String id;// 明细ID
	private String costname;// 费用名称
	private String ccus_id;// 客户编码
	private String csup_id;// 供应商编码
	private String citem_id;// 项目编码
	private String cper_id;// 人员编码
	private String money;// 金额
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCostname() {
		return costname;
	}
	public void setCostname(String costname) {
		this.costname = costname;
	}
	public String getCcus_id() {
		return ccus_id;
	}
	public void setCcus_id(String ccus_id) {
		this.ccus_id = ccus_id;
	}
	public String getCsup_id() {
		return csup_id;
	}
	public void setCsup_id(String csup_id) {
		this.csup_id = csup_id;
	}
	public String getCitem_id() {
		return citem_id;
	}
	public void setCitem_id(String citem_id) {
		this.citem_id = citem_id;
	}
	public String getCper_id() {
		return cper_id;
	}
	public void setCper_id(String cper_id) {
		this.cper_id = cper_id;
	}
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}

	

}
